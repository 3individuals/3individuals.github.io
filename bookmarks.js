function expand_outer_box(){
    document.getElementById("box").style.display="block";
    document.getElementById("bookmark_title").style.display="none";
    document.getElementById("add_button").style.display="block";
}

function shrink_outer_box(){
    document.getElementById("box").style.display="none";
    document.getElementById("bookmark_title").style.display="block";
    document.getElementById("add_button").style.display="none";
}